package entity;

public class alarmBean {
	
	private int alarm_num;
	private String alarm_user_id;
	private String alarm_con;
	private int alarm_check;
	
	public int getAlarm_num() {
		return alarm_num;
	}
	public void setAlarm_num(int alarm_num) {
		this.alarm_num = alarm_num;
	}
	public String getAlarm_user_id() {
		return alarm_user_id;
	}
	public void setAlarm_user_id(String alarm_user_id) {
		this.alarm_user_id = alarm_user_id;
	}
	public String getAlarm_con() {
		return alarm_con;
	}
	public void setAlarm_con(String alarm_con) {
		this.alarm_con = alarm_con;
	}
	public int getAlarm_check() {
		return alarm_check;
	}
	public void setAlarm_check(int alarm_check) {
		this.alarm_check = alarm_check;
	}

}
