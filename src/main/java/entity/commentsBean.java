package entity;

public class commentsBean {
	
	private int comment_num;
	private int comment_funding_num;
	private String comment_user_id;
	private String comment_con;
	private String comment_date;
	private String comment_ans;
	
	public int getComment_num() {
		return comment_num;
	}
	public void setComment_num(int comment_num) {
		this.comment_num = comment_num;
	}
	public int getComment_funding_num() {
		return comment_funding_num;
	}
	public void setComment_funding_num(int comment_funding_num) {
		this.comment_funding_num = comment_funding_num;
	}
	public String getComment_user_id() {
		return comment_user_id;
	}
	public void setComment_user_id(String comment_user_id) {
		this.comment_user_id = comment_user_id;
	}
	public String getComment_con() {
		return comment_con;
	}
	public void setComment_con(String comment_con) {
		this.comment_con = comment_con;
	}
	public String getComment_date() {
		return comment_date;
	}
	public void setComment_date(String comment_date) {
		this.comment_date = comment_date;
	}
	public String getComment_ans() {
		return comment_ans;
	}
	public void setComment_ans(String comment_ans) {
		this.comment_ans = comment_ans;
	}

}
