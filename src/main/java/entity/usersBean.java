package entity;

public class usersBean {
	
	private String user_id;
	private String user_pw;
	private String user_name;
	private String user_resnum;
	private String user_phone;
	private String user_address;
	private int user_master;
	private String user_info;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pw() {
		return user_pw;
	}
	public void setUser_pw(String user_pw) {
		this.user_pw = user_pw;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_resnum() {
		return user_resnum;
	}
	public void setUser_resnum(String user_resnum) {
		this.user_resnum = user_resnum;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getUser_address() {
		return user_address;
	}
	public void setUser_address(String user_address) {
		this.user_address = user_address;
	}
	public int getUser_master() {
		return user_master;
	}
	public void setUser_master(int user_master) {
		this.user_master = user_master;
	}
	public String getUser_info() {
		return user_info;
	}
	public void setUser_info(String user_info) {
		this.user_info = user_info;
	}

}
