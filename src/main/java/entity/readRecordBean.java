package entity;

public class readRecordBean {

	private int read_num;
	private int read_funding_num;
	private String read_user_id;
	private int read_wish;
	
	public int getRead_num() {
		return read_num;
	}
	public void setRead_num(int read_num) {
		this.read_num = read_num;
	}
	public int getRead_funding_num() {
		return read_funding_num;
	}
	public void setRead_funding_num(int read_funding_num) {
		this.read_funding_num = read_funding_num;
	}
	public String getRead_user_id() {
		return read_user_id;
	}
	public void setRead_user_id(String read_user_id) {
		this.read_user_id = read_user_id;
	}
	public int getRead_wish() {
		return read_wish;
	}
	public void setRead_wish(int read_wish) {
		this.read_wish = read_wish;
	}
	
}
