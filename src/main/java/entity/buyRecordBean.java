package entity;

public class buyRecordBean {
	
	private int buy_num;
	private int buy_price_num;
	private String buy_user_id;
	private String buy_date;
	
	public int getBuy_num() {
		return buy_num;
	}
	public void setBuy_num(int buy_num) {
		this.buy_num = buy_num;
	}
	public int getBuy_price_num() {
		return buy_price_num;
	}
	public void setBuy_price_num(int buy_price_num) {
		this.buy_price_num = buy_price_num;
	}
	public String getBuy_user_id() {
		return buy_user_id;
	}
	public void setBuy_user_id(String buy_user_id) {
		this.buy_user_id = buy_user_id;
	}
	public String getBuy_date() {
		return buy_date;
	}
	public void setBuy_date(String buy_date) {
		this.buy_date = buy_date;
	}

}
