package entity;

public class couponBean {

	private int coupon_num;
	private String coupon_name;
	private int coupon_dc;
	private String coupon_date;
	
	public int getCoupon_num() {
		return coupon_num;
	}
	public void setCoupon_num(int coupon_num) {
		this.coupon_num = coupon_num;
	}
	public String getCoupon_name() {
		return coupon_name;
	}
	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}
	public int getCoupon_dc() {
		return coupon_dc;
	}
	public void setCoupon_dc(int coupon_dc) {
		this.coupon_dc = coupon_dc;
	}
	public String getCoupon_date() {
		return coupon_date;
	}
	public void setCoupon_date(String coupon_date) {
		this.coupon_date = coupon_date;
	}
	
}
