package entity;

public class reviewBean {
	
	private int review_num;
	private int review_funding_num;
	private String review_user_id;
	private String review_con;
	private String review_date;
	
	public int getReview_num() {
		return review_num;
	}
	public void setReview_num(int review_num) {
		this.review_num = review_num;
	}
	public int getReview_funding_num() {
		return review_funding_num;
	}
	public void setReview_funding_num(int review_funding_num) {
		this.review_funding_num = review_funding_num;
	}
	public String getReview_user_id() {
		return review_user_id;
	}
	public void setReview_user_id(String review_user_id) {
		this.review_user_id = review_user_id;
	}
	public String getReview_con() {
		return review_con;
	}
	public void setReview_con(String review_con) {
		this.review_con = review_con;
	}
	public String getReview_date() {
		return review_date;
	}
	public void setReview_date(String review_date) {
		this.review_date = review_date;
	}

}
