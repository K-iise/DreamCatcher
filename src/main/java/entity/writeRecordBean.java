package entity;

public class writeRecordBean {

	private int write_num;
	private int write_funding_num;
	private String write_user_id;
	private int write_wish;
	
	public int getWrite_num() {
		return write_num;
	}
	public void setWrite_num(int write_num) {
		this.write_num = write_num;
	}
	public int getWrite_funding_num() {
		return write_funding_num;
	}
	public void setWrite_funding_num(int write_funding_num) {
		this.write_funding_num = write_funding_num;
	}
	public String getWrite_user_id() {
		return write_user_id;
	}
	public void setWrite_user_id(String write_user_id) {
		this.write_user_id = write_user_id;
	}
	public int getWrite_wish() {
		return write_wish;
	}
	public void setWrite_wish(int write_wish) {
		this.write_wish = write_wish;
	}
	
}
